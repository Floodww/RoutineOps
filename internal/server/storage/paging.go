package storage

// Постраничные выдачи. Единый clamp на всех, чтобы `?limit=2000000000` не
// материализовал таблицу в память (тот же приём уже стоял точечно в ListAuditLog
// и ListScriptResultsByPolicy — здесь он общий).
//
// Счётчик total берём окном COUNT(*) OVER() прямо в запросе страницы, а не вторым
// SELECT COUNT(*): один поход в базу и, главное, страница со счётчиком согласованы.
// Отдельный запрос под нагрузкой считал бы уже другой набор строк — постранично
// это видно как «пропавшая» или продублированная запись при листании.
//
// Плата за окно: total едет ВМЕСТЕ со строками, поэтому пустая страница (offset
// уехал за конец выдачи — записи удалили, пока оператор листал) отдаёт total=0, а не
// реальное число. Отличить это от честного «ничего не найдено» можно по offset>0;
// интерфейс в таком случае возвращается на первую страницу и перезапрашивает.
// ponytail: чинится вторым COUNT-запросом только для пустой страницы — не делаем,
// пока пустая страница остаётся следствием гонки, а не нормальным сценарием.
const (
	DefaultPageLimit = 50
	MaxPageLimit     = 500
)

// clampPage приводит limit/offset к безопасным значениям: 0 и мусор → дефолт,
// потолок жёсткий, отрицательный offset → 0.
func clampPage(limit, offset int) (int, int) {
	if limit <= 0 || limit > MaxPageLimit {
		limit = DefaultPageLimit
	}
	if offset < 0 {
		offset = 0
	}
	return limit, offset
}
