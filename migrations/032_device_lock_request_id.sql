-- 032: привязка отчёта о снятии блокировки к КОНКРЕТНОМУ локу.
--
-- Агент шлёт ReportLockStatus(UNLOCKED) через durable-outbox: отчёт переживает
-- офлайн и рестарт, поэтому может доехать ПОСЛЕ того, как IT выдал НОВУЮ
-- блокировку. Раньше такой устаревший отчёт безусловно чистил желаемое состояние
-- (SetDeviceLockState 'unlocked', ''), то есть молча разоружал свежий kill-switch:
-- ноутбук уехал офлайн со снятым локом в очереди, IT запер заново другим паролем,
-- машина вышла в сеть — и новый лок исчез. Единственный гард в этой ветке
-- (gateway) закрывал только FileVault-режим, overlay проваливался насквозь.
--
-- lock_request_id хранит идентификатор лока, которым выдано ТЕКУЩЕЕ желаемое
-- состояние: id lock-задачи (push-путь; сервер кладёт его же в LockCommand.RequestId,
-- см. worker). На pull-пути агент применяет лок сам и использует в качестве
-- идентификатора lock_hash — сверка идёт по нему, отдельного хранения не требует.
--
-- Пусто = лок выдан до этой миграции: отличить устаревший отчёт от свежего нельзя,
-- поэтому гейт такие пропускает как раньше. Исключение самозакрывается при
-- следующей выдаче блокировки.
ALTER TABLE devices ADD COLUMN IF NOT EXISTS lock_request_id TEXT NOT NULL DEFAULT '';
